void HAL_SPI_TxRxCpltCallback(SPI_HandleTypeDef *hspi)
{
   if(hspi->Instance == SPI1)
   {
	   HAL_GPIO_WritePin(bmi270.cs_port, bmi270.cs_pin, GPIO_PIN_SET);


	   memcpy(local_rx,(uint8_t*)bmi270.spi_rx_buffer,8);

	   bmi270.raw_x = (int16_t)(((uint8_t)local_rx[3] << 8) | (uint8_t)local_rx[2]);
	   bmi270.raw_y = (int16_t)(((uint8_t)local_rx[5] << 8) | (uint8_t)local_rx[4]);
	   bmi270.raw_z = (int16_t)(((uint8_t)local_rx[7] << 8) | (uint8_t)local_rx[6]);



	   raw_gyro_x = ((float)bmi270.raw_x / 16.4f) - bmi270.offset_x;
	   raw_gyro_y = ((float)bmi270.raw_y / 16.4f) - bmi270.offset_y;
	   raw_gyro_z = ((float)bmi270.raw_z / 16.4f) - bmi270.offset_z;
	   bmi270.gyro_x = Biquad_Update(raw_gyro_x, &f.x1_x, &f.x2_x, &f.y1_x, &f.y2_x, &f);
	   bmi270.gyro_y = Biquad_Update(raw_gyro_y, &f.x1_y, &f.x2_y, &f.y1_y, &f.y2_y, &f);
	   bmi270.gyro_z = Biquad_Update(raw_gyro_z, &f.x1_z, &f.x2_z, &f.y1_z, &f.y2_z, &f);
 	   y++;
   }

}
